package modele;

public class Hole {

	private int id;
	private int[] nodes;
	private int id_building;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int[] getNodes() {
		return nodes;
	}

	public void setNodes(int[] nodes) {
		this.nodes = nodes;
	}

	public int getId_building() {
		return id_building;
	}

	public void setId_building(int id_building) {
		this.id_building = id_building;
	}
}
